npm install package-name     # Add new package
npm uninstall package-name   # Remove package
npm update                   # Update all packages to newer versions
npm list                     # See what's installed

npm start                    # Usually starts your development server
npm test                     # Runs your tests
npm run build               # Usually builds your project for production

npm info package-name        # See info about a package
npm outdated                 # See which packages have newer versions